	// 数组不会比较细致的检查，多维数组存在。 因此，判断的时候，只会判断外面 一层的 结构体。
	
	
	// 先 引用 后 赋值
	$arr = array(0, 1, 2, 3);
	
	$x = &$arr[1];
	
	$tmp = $arr;
	
	$arr[1] = 999;
	
	echo $tmp[1]; // 999 . hash表中的zvalue结构体中会变成引用类型。  // 只去关注外面一层结构体，而不去关注 hash表中的值。
	
	
	echo '<br/>';
	
	// 先赋值，后引用
	$arr = array(0, 1, 2, 3);
	
	$tmp = $arr;
	
	$x = &$arr[1];
	
	$arr[1] = 999;
	
	echo $tmp[1]; // 1 	